									<h6 class="h6_mediumspecial">The CTTC Today</h6>
									<p class="p_descripcion">The CTTC now works with ten weaving communities from the Cusco region: Accha Alta, Acopia, Chahuaytire, Chinchero, Mahuaypampa, Huacatinco, Patabamba, Pitumarca, Santa Cruz de Sallac, and Santo Tomas. Huacatinco, from the Ocongate region, was the latest community to partner with the CTTC in 2012. Each one maintains unique ancestral styles, techniques and designs that the weavers are working hard to maintain. As of today the CTTC and the weavers have rediscovered the process of natural dyes and recovered hundreds of designs and techniques that were almost forgotten.<br><br> The CTTC also runs an Education Department whose mission is to “provide an interactive space for weavers and the public through programs, investigations, and publications that promote and revalue ancestral textiles.” The Center has published four books, regularly attends national and international art festivals and speaking events, participates in national and international museum exhibits, and organizes educational events for the weavers, their families, and the public. The largest event the CTTC holds is Tinkuy: A Gathering of Weavers, an international conference in the city of Cusco that brings together textile artists and enthusiasts from across the globe.<br><br> As it was in the beginning, the Center’s most important work is with the Young Weavers Groups. Each Saturday children and young people gather in their community weaving center to learn from their elders. It is through educating younger generations that the CTTC hopes to ensure a future for textile traditions in the Cusco region.</p>
