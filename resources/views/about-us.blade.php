									<h6 class="h6_mediumspecial">The Future of the CTTC</h6>
									<p class="p_descripcion">Since 1996, the Center has watched a transformation play out in the communities and in the city of Cusco. Where before indigenous people were ashamed of their identity and traditions, they are now proud of their textiles, proud of their community, and most importantly, proud of themselves.<br><br> The hope of the CTTC is that this momentum not stop at the borders of Peru, but that the world should come to know and respect these weavers as artists and their textile traditions as world patrimony. Through continued investigations, educational programs, and work with the weavers, the CTTC hopes that one day this dream will become a reality.</p>
