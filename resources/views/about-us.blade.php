									<h6 class="h6_mediumspecial">Initial Community Organization and Development: 1970s – 1990s</h6>
									<p class="p_descripcion">The story of the Centro de Textiles Tradicionales del Cusco began with a handful of women who started to gather in each other’s homes to spin and weave in the 1970s. These Quechua weavers from the community of Chinchero realized that Cusqueñan textile traditions were beginning to disappear. People preferred the convenience of cheap chemical dyes to expensive natural dyes. The more complicated patterns and techniques were at risk of extinction as younger generations failed to learn from older relatives. Racism against indigenous people also meant that women and men were ashamed or afraid to use their traditional clothing.<br><br> Worried that their sacred textile tradition was in danger of disappearing forever, the weavers decided that the fate of their traditions was in their own hands. They came together to try and recover old designs, relearn ancient techniques, and sell their textiles to tourists as a way to support themselves independently from male relatives.<br><br> At this time, an ethnobotanist and anthropologist from the United States moved to Chinchero with their small children. The weavers taught them how important weaving was to every aspect of daily, spiritual and community life. They also met a young weaver and spinner, Nilda Callanaupa, who became their weaving teacher. Along with the weavers they founded a community cultural center in Chinchero. Working together throughout the 1980s and 1990s, Nilda, the weavers and their supporters also formed a young weaver’s group to recover traditional designs and techniques from Chinchero that were being lost.</p>
