									<h6 class="h6_mediumspecial">The Birth of a Non-Profit: 1996</h6>
									<p class="p_descripcion">An endeavor as large as reviving traditions and cultural pride is not easily undertaken, and the community cultural center in Chinchero floundered. Undeterred, the weavers, led by Nilda Callañaupa, revised their goals, and with many international supporters, founded the Centro de Textiles Tradicionales del Cusco (CTTC) in 1996 as a non-profit association. After much work, the CTTC became an official non-profit organization, an NGO, registered in Peru in 1998. The goal of the new NGO, which was based in the city of Cusco, was to work with various communities from the Cusco region to revive textile traditions and empower weavers, especially women.<br><br> To begin with, the CTTC partnered with a small number of communities, including Chinchero, Chahuaytire and Pitumarca. In each community the first goal was to work with community elders to recover the designs, techniques, and knowledge they knew and help them teach it to other weavers. The second goal was to build a weaving center in each community where women and men could come to meet and work, free from the distractions of home life and sheltered from the rain. As the skill of the weavers improved and they created traditional textiles in increasing numbers, it became a priority to market them.<br><br> Through the support of international donors and foundations, the CTTC was able to build weaving centers in partner communities, hold workshops with the weavers, and open a store, office and museum in Cusco, Peru.</p>
